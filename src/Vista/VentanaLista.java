    private void showButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showButtonActionPerformed
        try {
            ArrayList<Cancion> lista = controlador.obtenerCanciones();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            int fila = 0, columna = 0;
            for (Cancion song : lista) {
                model.setRowCount(model.getRowCount() + 1);
                model.setValueAt(song.getTitle(), fila, columna);
                model.setValueAt(song.getAlbum(), fila, columna + 1);
                model.setValueAt(song.getArtist(), fila, columna + 2);
                model.setValueAt(song.getDuration(), fila, columna + 3);
                fila++;
                columna = 0;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Tienes que conectarte a la Base De Datos", "Error", JOptionPane.ERROR);
        }
    }//GEN-LAST:event_showButtonActionPerformed

