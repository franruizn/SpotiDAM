    private void insertButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertButtonActionPerformed
        String title = JOptionPane.showInputDialog("Introduce el titulo");
        if (!title.isEmpty()) {
            String album = JOptionPane.showInputDialog("Introduce el album");
            if (!album.isEmpty()) {
                String artist = JOptionPane.showInputDialog("Introduce el artista");
                if (!artist.isEmpty()) {
                    String duration = JOptionPane.showInputDialog("Introduce la duracion (m:s)");
                    if (!duration.isEmpty()) {
                        String consulta = "INSERT INTO songs (Título,Álbum,Artista,Duración) VALUES ('" + title + "','" + album + "','" + artist + "','" + duration + "')";

                        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                        try {
                            conexion.ejecutarInsertDeleteUpdate(consulta);
                            JOptionPane.showMessageDialog(null, "Objeto insertado correctamente", "Objeto insertado", JOptionPane.INFORMATION_MESSAGE);
                            model.setRowCount(0);
                            showButtonActionPerformed(evt);
                        } catch (SQLException ex) {
                            System.out.println("No se puede añadir la cancion");
                        }

                    } else {
                        JOptionPane.showMessageDialog(null, "No se ha introducito ninguna duración", "Duración no válida", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "No se ha introducito ningún artista", "Artista no válido", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "No se ha introducito ningún album", "Album no válido", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "No se ha introducito ningún título", "Título no válido", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_insertButtonActionPerformed

